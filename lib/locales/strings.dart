  static String translate(BuildContext context, String key) {
    final locale = Localizations.localeOf(context);
    final languageCode = locale.languageCode;

    // Check if the translation exists for the selected language and key
    final localizedMap = localizedStrings[languageCode];
    if (localizedMap != null && localizedMap.containsKey(key)) {
      return localizedMap[key] ??
          key; // Return the translation or the key itself if translation is null
    } else {
      // If the translation doesn't exist, return a placeholder or the key itself
      return key; // Return the key itself or any other fallback message
    }
  }
